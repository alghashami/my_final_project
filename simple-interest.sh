#!/bin/bash

# برنامج حساب الفائدة البسيطة

echo "مرحبًا! هذا برنامج حساب الفائدة البسيطة"
echo "----------------------------------------"

# طلب إدخال المبلغ الأساسي
read -p "أدخل المبلغ الأساسي: " principal

# طلب إدخال نسبة الفائدة
read -p "أدخل نسبة الفائدة (سنوية): " rate

# طلب إدخال المدة الزمنية
read -p "أدخل المدة بالسنوات: " time

# حساب الفائدة البسيطة
interest=$(echo "scale=2; $principal * $rate * $time / 100" | bc)

# حساب المبلغ الإجمالي
total=$(echo "scale=2; $principal + $interest" | bc)

# عرض النتائج
echo "----------------------------------------"
echo "النتيجة:"
echo "الفائدة البسيطة: $interest"
echo "المبلغ الإجمالي: $total"
